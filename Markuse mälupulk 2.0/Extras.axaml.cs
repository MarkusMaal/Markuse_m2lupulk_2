        private async void Button_Click(object? sender, Avalonia.Interactivity.RoutedEventArgs e)
        {
            if (sender is not Button me)
            {
                return;
            }
            string? name = me.Content?.ToString();
            if (name == null)
            {
                return;
            }
            switch (name)
            {
                case "Mälupulga pakkfail":
                    LaunchBatchfile();
                    break;
                case "Turvarežiim":
                    SafeMode sm = new();
                    sm.Show();
                    this.Close();
                    parent?.Close();
                    break;
                case "Mälupulga universaalprogramm":
                    LaunchLegacy();
                    break;
                case "Teema kohandamine":
                    ManageTheme mt = new()
                    {
                        Background = this.Background,
                        Foreground = this.Foreground,
                        parent = parent,
                    };
                    if (parent != null)
                    {
                        mt.GradientsCheck.IsChecked = parent.GradientBg.Background?.Opacity > 0;
                    }
                    this.Opacity = 0;
                    await mt.ShowDialog(this).WaitAsync(CancellationToken.None);
                    this.Close();
                    break;
            }
        }

        private async void LaunchBatchfile() {
            if (parent == null) {return;}
            this.Hide();
            if (await parent.MessageBoxShow("Kas soovite sünkroonida juhtpaneeli andmed Markuse asjade pakkfailiga?", "Markuse asjade pakkfail", MsBox.Avalonia.Enums.ButtonEnum.YesNo, MsBox.Avalonia.Enums.Icon.Question) == MsBox.Avalonia.Enums.ButtonResult.Yes)
            {
                string Password = new Random().Next(0, 21482572).ToString();
                _ = parent.MessageBoxShow("Parool haldamiseks: " + Password + "\n\nPidage meeles, et selle parooliga mõjutate ainult seda, mis toimub pakkfailis ja mitte teistes mälupulga programmides.", "Markuse asjad pakkfail", MsBox.Avalonia.Enums.ButtonEnum.Ok, MsBox.Avalonia.Enums.Icon.Setting);
                RichTextBox rtb = new();
                List<string> pealkirjad = [];
                List<string> uudised = [];
                List<string> videod = [];
                for (int i = 1; i <= 5; i++)
                {
                    rtb.LoadRtfDoc(parent.flash_root + "/E_INFO/uudis" + i.ToString() + ".rtf");
                    string lines = rtb.FlowDoc.Text;
                    pealkirjad.Add(ToSuitableBatchString(lines.Split('\n')[0].Replace("\r", "")));
                    uudised.Add(ToSuitableBatchString(lines.Replace(lines.Split('\n')[0], "").Replace("\r\n", "").Replace("\n", "")));
                }
                try
                {
                    foreach (FileInfo fi in new DirectoryInfo(parent.flash_root + "/Markuse_videod").GetFiles())
                    {
                        string file = fi.Name;
                        videod.Add(ToSuitableBatchString(string.Concat(file.Split('.')[1].AsSpan(1), ".", file.Split('.')[2])));
                    }
                } catch { }
                string settings = "";
                settings += "SET rightpass=" + Password + "\r\n";
                int j = 0;
                foreach (string pealkiri in pealkirjad)
                {
                    j++;
                    settings += "SET uudis" + j.ToString() + "=" + pealkiri + "\r\n";
                }
                j = 0;
                foreach (string uudis in uudised)
                {
                    j++;
                    settings += "SET u" + j.ToString() + "=" + uudis + "\r\n";
                }
                j = 0;
                foreach (string video in videod)
                {
                    j++;
                    settings += "SET video" + j.ToString() + "=" + video + "\r\n";
                }
                settings += "SET selectedcol=07\r\n";
                settings += "SET adapp=\r\n";
                settings += "SET adpp=\r\n";
                settings += "SET ads=n\r\n";
                settings += "SET adtext=\r\n";
                if (File.Exists(parent.flash_root + "/E_INFO/Markuse asjad.bat"))
                {
                    File.Delete(parent.flash_root + "/E_INFO/Markuse asjad.bat");
                }
                if (File.Exists(parent.flash_root + "/E_INFO/markuse_asjade_seaded.cmd"))
                {
                    File.Delete(parent.flash_root + "/E_INFO/markuse_asjade_seaded.cmd");
                }
                EncodingProvider ppp = CodePagesEncodingProvider.Instance;
                Encoding.RegisterProvider(ppp);
                File.WriteAllBytes(parent.flash_root + "/E_INFO/Markuse asjad.bat", GetBatchfileBytes());
                File.WriteAllText(parent.flash_root + "/E_INFO/markuse_asjade_seaded.cmd", settings, ppp.GetEncoding("windows-1252"));
                File.SetAttributes(parent.flash_root + "/E_INFO/markuse_asjade_seaded.cmd", FileAttributes.Hidden);
                _ = parent.MessageBoxShow("Andmed sünkroniseeriti. Vajutage OK, et pakkfail käivitada\r\n\r\nSalafunktsioonide klahvid (vajuta enne käivitusanimatsiooni):\r\n" +
                                "M - arendaja režiim\r\n" +
                                "a - vanema pakkfaili liidese režiim\r\n" +
                                "r - pakkfaili seadete nullimine\r\n" +
                                "k - kiirlaadimine\r\n" +
                                "u - käsurida, kirjutage \"exit\", et jätkata pakkfaili käivitamist\r\n" +
                                "s - pakkfaili turvarežiim (ära laadi uusimaid videosid\r\n" +
                                "e - katkesta pakkfaili käivitamine\r\n" +
                                "n - käivita tavarežiimis, valitakse 1 sek jooksul automaatselt", "Markuse asjade pakkfail", MsBox.Avalonia.Enums.ButtonEnum.Ok, MsBox.Avalonia.Enums.Icon.Success);
            }
            else
            {
                if (File.Exists(parent.flash_root + "/E_INFO/Markuse asjad.bat"))
                {
                    File.Delete(parent.flash_root + "/E_INFO/Markuse asjad.bat");
                }
                if (File.Exists(parent.flash_root + "/E_INFO/markuse_asjade_seaded.cmd"))
                {
                    File.Delete(parent.flash_root + "/E_INFO/markuse_asjade_seaded.cmd");
                }
                try
                {
                    File.SetAttributes(parent.flash_root + "/E_INFO/markuse_asjade_seaded.cmd", FileAttributes.Hidden);
                } catch { }
            }
            try
            {
                File.WriteAllBytes(parent.flash_root + "/E_INFO/Markuse asjad.bat", GetBatchfileBytes());
                if (OperatingSystem.IsWindows()) {
                    Process p = new();
                    p.StartInfo.FileName = parent.flash_root + "/E_INFO/Markuse asjad.bat";
                    p.StartInfo.WorkingDirectory = parent.flash_root + "/E_INFO";
                    p.StartInfo.UseShellExecute = true;
                    p.Start();
                } else { // most likely won't work correctly under wine, but you might as well try it out :)
                    Process p = new() {
                        StartInfo = new () {
                            FileName = "wine",
                            Arguments = "cmd /c Z:" + parent.flash_root.Replace("/", "\\") + "\\E_INFO\\Markuse asjad.bat",
                            WorkingDirectory = parent.flash_root + "/E_INFO",
                            UseShellExecute = true
                        }
                    };
                    p.Start();
                }
            } catch (Exception ex)
            {
                _ = parent.MessageBoxShow(ex.Message, "Viga", MsBox.Avalonia.Enums.ButtonEnum.Ok, MsBox.Avalonia.Enums.Icon.Error);
            }
            this.Show();
        }

        private static string ToSuitableBatchString(string s) {
            return s.Replace("ä", "„").Replace("õ", "ä").Replace("ö", "”").Replace("ü", "").Replace("ž", "Ų").Replace("\"", "'").Replace("%", "%%").Replace("|", "^|").Replace("<", "^<").Replace(">", "^>");
        }

        private async void LaunchLegacy() {
            if (parent == null) {
                return;
            }
            this.Hide();
            if (await parent.MessageBoxShow("See toiming sulgeb mälupulga juhtpaneeli. Kas olete kindel, et soovite jätkata?", "Mälupulga universaalprogramm", MsBox.Avalonia.Enums.ButtonEnum.YesNo, MsBox.Avalonia.Enums.Icon.Warning) == MsBox.Avalonia.Enums.ButtonResult.Yes)
            {
                if (File.Exists($"{parent.mas_root}/running.log"))
                {
                    if (await parent.MessageBoxShow("Markuse mälupulga universaalprogramm on juba avatud. Vajutage \"OK\", et universaalprogramm sunniviisiliselt käivitada või \"Katkesta\", et toiming katkestada", "Mälupulga universaalprogramm", MsBox.Avalonia.Enums.ButtonEnum.OkAbort, MsBox.Avalonia.Enums.Icon.Error) == MsBox.Avalonia.Enums.ButtonResult.Ok)
                    {
                        File.Delete($"{parent.mas_root}/running.log");
                    }
                    else
                    {
                        return;
                    }
                }
                try
                {
                    Process p = new();
                    if (OperatingSystem.IsWindows()) {
                        p.StartInfo.FileName = $"{parent.flash_root}/E_INFO/universaalprogramm.exe";
                    } else {
                        p.StartInfo.FileName = "wine";
                        p.StartInfo.Arguments = $"{parent.flash_root}/E_INFO/universaalprogramm.exe";
                    }
                    p.Start();
                    Environment.Exit(0);
                    return;
                }
                catch
                {
                    _ = parent.MessageBoxShow("Faili kirjutamine nurjus. Mälupulga univesaalprogrammi ei saanud käivitada", "Mälupulga universaalprogramm", MsBox.Avalonia.Enums.ButtonEnum.Ok, MsBox.Avalonia.Enums.Icon.Error);
                }
            }
            this.Show();
        }

